function localStorageLevel(update) {
  if (update) {
    localStorage.setItem("wmt-level", update);
    return update;
  }
  let levelLS = localStorage.getItem("wmt-level");
  if (!levelLS) {
    localStorageLevel(1);
    return 1;
  }
  return levelLS;
}

function changePage(page) {
  document.body.style.transition = "opacity .3s ease";
  document.body.style.opacity = 0;
  setTimeout(() => {
    document.body.prepend(page)
    document.body.style.opacity = 1
  }, 351);
}

function startGame() {
  index = 1;
  wordsToMemorize = [];
  memoList.innerHTML = "";
  
  let numOfWords = 3 + level;
  for (let i = 0; i < numOfWords; i++) {
    if (Math.random() < 0.25) {
      wordsToMemorize.push(verbs[Math.floor(Math.random() * verbs.length)]);
    } else {
      wordsToMemorize.push(nouns[Math.floor(Math.random() * nouns.length)]);
    }
  }
  console.log(wordsToMemorize);
  wordVar.innerText = wordsToMemorize[index - 1];
  positionVar.innerText = index;
  wordsLengthVars.forEach(wordsLengthVar =>
    wordsLengthVar.innerText = wordsToMemorize.length
  );
}

function nextWordToMemorize() {
  index++;
  if (index > wordsToMemorize.length) {
    changePage(searchWordsPage);
    index = 1;
    return;
  }
  wordVar.innerText = wordsToMemorize[index - 1];
  positionVar.innerText = index;
}

